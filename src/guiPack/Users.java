package guiPack;

public class Users {
	int uID;
	String uName;
	String pass;
	int phoneNo;
	String address;
	int totalOrder;
	int point;
	public Users(int uID, String uName, String pass, int phoneNo, String address, int totalOrder, int point) {
		super();
		this.uID = uID;
		this.uName = uName;
		this.pass = pass;
		this.phoneNo = phoneNo;
		this.address = address;
		this.totalOrder = totalOrder;
		this.point = point;
	}


}
